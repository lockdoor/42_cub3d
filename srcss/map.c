int **make_map(void)
{
	int	**map;

	map = (int **) malloc (sizeof(int *) * MAP_H);
	// printf("map: %p\n", map);
	for (int i = 0; i < MAP_H; i++)
	{
		map[i] = (int *) malloc (sizeof(int) * MAP_W);
		for (int j = 0; j < MAP_W; j++)
			map[i][j] = worldMap[i][j];
	}
	// printf("map: %p\n", map);
	return (map);
}
